Byte * AppearanceDataToCache(Byte *data) {
    int message = data[0];
    int subRelevantRange = data[1];
    Byte interval = data[2];
    int indicator = data[3];
    if (!message) return data + indicator;
    for (int i = indicator; i < indicator + subRelevantRange; i++) {
        int value = data[i] + interval;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[indicator + subRelevantRange] = 0;
    return data + indicator;
}

NSString *StringFromAppearanceData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)AppearanceDataToCache(data)];
}
